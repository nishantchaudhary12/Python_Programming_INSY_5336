import turtle

turtle.circle(20)
turtle.penup()
turtle.goto(0,20)
turtle.pendown()
turtle.forward(100)
turtle.penup()
turtle.forward(10)
turtle.pendown()
turtle.write("East")
turtle.penup()
turtle.goto(0,20)
turtle.pendown()
turtle.left(90)
turtle.forward(100)
turtle.penup()
turtle.forward(10)
turtle.pendown()
turtle.write("North")
turtle.penup()
turtle.goto(0,20)
turtle.pendown()
turtle.left(90)
turtle.forward(100)
turtle.penup()
turtle.forward(10)
turtle.pendown()
turtle.write("West")
turtle.penup()
turtle.goto(0,20)
turtle.pendown()
turtle.left(90)
turtle.forward(100)
turtle.penup()
turtle.forward(10)
turtle.pendown()
turtle.write("South")

turtle.hideturtle()
turtle.done()







